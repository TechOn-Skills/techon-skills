/** Get course slug from a course's first CHAPTER_1_MD_PATH (or any chapter path). */
export function getCourseSlugFromLectureRoutes(courseRoutes: Record<string, Record<string, string>>): string {
    const firstModule = Object.values(courseRoutes)[0];
    if (!firstModule) return "";
    const firstPath = Object.values(firstModule)[0];
    if (!firstPath || typeof firstPath !== "string") return "";
    const segment = firstPath.replace(/^\//, "").split("/")[0];
    return segment ?? "";
}

export type ChapterEntry = { moduleSlug: string; chapterSlug: string; chapterNumber: number; title: string };

/** Get flat list of chapters for a course's routes. */
export function getChaptersFromCourseRoutes(courseRoutes: Record<string, Record<string, string>>): ChapterEntry[] {
    const chapters: ChapterEntry[] = [];
    for (const [moduleSlug, modulePaths] of Object.entries(courseRoutes)) {
        const mdKeys = Object.keys(modulePaths).filter((k) => k.endsWith("_MD_PATH"));
        for (const key of mdKeys) {
            const path = modulePaths[key];
            if (!path || typeof path !== "string") continue;
            const base = path.replace(/^\//, "").split("/").pop() ?? "";
            const chapterSlug = base.replace(/\.md$/, "");
            const numMatch = key.match(/CHAPTER_(\d+)_MD_PATH/);
            const chapterNumber = numMatch ? parseInt(numMatch[1], 10) : 0;
            const title = `Chapter ${chapterNumber}: ${chapterSlug.replace(/_/g, " ")}`;
            chapters.push({ moduleSlug, chapterSlug, chapterNumber, title });
        }
    }
    return chapters.sort((a, b) => {
        if (a.moduleSlug !== b.moduleSlug) return a.moduleSlug.localeCompare(b.moduleSlug);
        return a.chapterNumber - b.chapterNumber;
    });
}

/** Find course routes by course slug (first path segment). */
export function getCourseRoutesBySlug(slug: string): Record<string, Record<string, string>> | null {
    for (const routes of Object.values(LECTURE_ROUTES)) {
        if (getCourseSlugFromLectureRoutes(routes) === slug) return routes as Record<string, Record<string, string>>;
    }
    return null;
}

export type ModuleWithChapters = { moduleSlug: string; moduleTitle: string; chapters: ChapterEntry[] };

/** Get chapters grouped by module, preserving module order. */
export function getChaptersByModuleFromCourseRoutes(
    courseRoutes: Record<string, Record<string, string>>
): ModuleWithChapters[] {
    const byModule: Record<string, ChapterEntry[]> = {};
    for (const [moduleSlug, modulePaths] of Object.entries(courseRoutes)) {
        const chapters: ChapterEntry[] = [];
        const mdKeys = Object.keys(modulePaths).filter((k) => k.endsWith("_MD_PATH"));
        for (const key of mdKeys) {
            const path = modulePaths[key];
            if (!path || typeof path !== "string") continue;
            const base = path.replace(/^\//, "").split("/").pop() ?? "";
            const chapterSlug = base.replace(/\.md$/, "");
            const numMatch = key.match(/CHAPTER_(\d+)_MD_PATH/);
            const chapterNumber = numMatch ? parseInt(numMatch[1], 10) : 0;
            const title = `Chapter ${chapterNumber}`;
            chapters.push({ moduleSlug, chapterSlug, chapterNumber, title });
        }
        chapters.sort((a, b) => a.chapterNumber - b.chapterNumber);
        if (chapters.length > 0) byModule[moduleSlug] = chapters;
    }
    const moduleOrder = Object.keys(courseRoutes);
    return moduleOrder
        .filter((slug) => byModule[slug]?.length)
        .map((moduleSlug) => ({
            moduleSlug,
            moduleTitle: moduleSlug.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase()),
            chapters: byModule[moduleSlug] ?? [],
        }));
}

/** Get the next chapter URL and label after the given chapter; null if course is complete. */
export function getNextChapter(
    courseSlug: string,
    moduleSlug: string,
    chapterSlug: string
): { url: string; label: string } | null {
    const courseRoutes = getCourseRoutesBySlug(courseSlug);
    if (!courseRoutes) return null;
    const flat = getChaptersFromCourseRoutes(courseRoutes);
    const idx = flat.findIndex((c) => c.moduleSlug === moduleSlug && c.chapterSlug === chapterSlug);
    if (idx < 0 || idx >= flat.length - 1) return null;
    const next = flat[idx + 1];
    const url = `/student/course/${courseSlug}/${next.moduleSlug}/${next.chapterSlug}`;
    const moduleTitle = next.moduleSlug.replace(/_/g, " ").replace(/\b\w/g, (c) => c.toUpperCase());
    const label = next.moduleSlug !== moduleSlug ? `${moduleTitle} – ${next.title}` : next.title;
    return { url, label };
}